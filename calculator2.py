num1 = float(input("Enter a number: "))
num2 = float(input("Enter a second number: "))
operator = input("Enter an operator: ")

if operator == "+":
    print(num1 + num2)

elif operator == "-":
    print(num2 - num1)

elif operator == "*":
    print(num1 * num2 )

elif operator == "/":
    print(num1 / num2)

else:
    print("Invalid operator!")








